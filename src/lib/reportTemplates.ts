export type SectionType = 'summary' | 'kpis' | 'equity-chart' | 'drawdown-chart' | 'trades-table' | 
  'monthly-heatmap' | 'distribution' | 'risk-metrics' | 'custom-text' | 'comparison' | 'annotations';

export interface ReportSection {
  id: string;
  type: SectionType;
  title: string;
  enabled: boolean;
  order: number;
  config?: Record<string, unknown>;
}

export interface ReportBranding {
  logoUrl?: string;
  companyName?: string;
  primaryColor?: string;
  secondaryColor?: string;
  fontFamily?: string;
  headerText?: string;
  footerText?: string;
  showWatermark?: boolean;
  showLogo?: boolean;
}

export interface ReportConfig {
  id: string;
  name: string;
  title: string;
  isPremium?: boolean;
  sections: ReportSection[];
  branding?: ReportBranding;
}

export interface ReportTemplate {
  id: string;
  name: string;
  description: string;
  sections: ReportSection[];
  branding: ReportBranding;
  format: 'pdf' | 'html' | 'excel';
  isDefault?: boolean;
}

export interface ScheduledReport {
  id: string;
  templateId: string;
  name: string;
  schedule: 'daily' | 'weekly' | 'monthly' | 'on-complete';
  recipients: string[];
  enabled: boolean;
  lastRun?: string;
  nextRun?: string;
}

