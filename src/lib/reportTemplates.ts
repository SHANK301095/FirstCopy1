export function generateReportHTML(
  template: ReportTemplate,
  data: Record<string, unknown>
): string {
  const { branding, sections } = template;
  
  const enabledSections = sections
    .filter(s => s.enabled)
    .sort((a, b) => a.order - b.order);
  
  const sectionsHTML = enabledSections.map(section => {
    switch (section.type) {
      case 'summary':
        return generateSummarySection(section, data, branding);
      case 'kpis':
        return generateKPIsSection(section, data, branding);
      case 'equity-chart':
        return generateEquityChartSection(section, data, branding);
      case 'drawdown-chart':
        return generateDrawdownSection(section, data, branding);
      case 'trades-table':
        return generateTradesSection(section, data, branding);
      case 'monthly-heatmap':
        return generateHeatmapSection(section, data, branding);
      case 'risk-metrics':
        return generateRiskSection(section, data, branding);
      default:
        return '';
    }
  }).join('\n');
  
  return `
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <title>${branding.headerText || 'Report'}</title>
  <style>
    body {
      font-family: ${branding.fontFamily};
      max-width: 1200px;
      margin: 0 auto;
      padding: 40px;
      background: #0a0e14;
      color: #e4e4e7;
      line-height: 1.6;
    }
    h1, h2, h3 { color: ${branding.primaryColor}; }
    .header {
      display: flex;
      align-items: center;
      gap: 20px;
      margin-bottom: 40px;
      padding-bottom: 20px;
      border-bottom: 2px solid ${branding.primaryColor}33;
    }
    .header img { height: 60px; }
    .section {
      margin-bottom: 40px;
      padding: 24px;
      background: #14181f;
      border-radius: 12px;
      border: 1px solid #1f2937;
    }
    .kpi-grid {
      display: grid;
      grid-template-columns: repeat(4, 1fr);
      gap: 16px;
    }
    .kpi-card {
      padding: 16px;
      background: #1a1f2e;
      border-radius: 8px;
      text-align: center;
    }
    .kpi-value {
      font-size: 24px;
      font-weight: bold;
      color: ${branding.primaryColor};
    }
    .kpi-label { color: #888; font-size: 12px; }
    .positive { color: #22c55e; }
    .negative { color: #ef4444; }
    table {
      width: 100%;
      border-collapse: collapse;
    }
    th, td {
      padding: 10px;
      text-align: left;
      border-bottom: 1px solid #333;
    }
    th { background: #1a1f2e; color: ${branding.primaryColor}; }
    .footer {
      margin-top: 40px;
      padding-top: 20px;
      border-top: 1px solid #333;
      text-align: center;
      color: #666;
      font-size: 12px;
    }
    ${branding.showWatermark ? `
    body::after {
      content: "${branding.companyName}";
      position: fixed;
      bottom: 20px;
      right: 20px;
      opacity: 0.1;
      font-size: 48px;
      transform: rotate(-15deg);
    }
    ` : ''}
  </style>
</head>
<body>
  <div class="header">
    ${branding.logoUrl ? `<img src="${branding.logoUrl}" alt="Logo" />` : ''}
    <div>
      <h1>${branding.headerText || 'Performance Report'}</h1>
      <p>${branding.companyName || ''} | Generated: ${new Date().toLocaleString()}</p>
    </div>
  </div>
  
  ${sectionsHTML}
  
  <div class="footer">
    ${branding.footerText || 'Generated by MMC AI Intelligence'}
  </div>
</body>
</html>`;
}

function generateSummarySection(section: ReportSection, data: Record<string, unknown>, branding: ReportBranding): string {
  const symbol = data.symbol || 'Unknown';
  const dateRange = data.dateRange || '';
  const netProfit = (data.netProfit as number) || 0;
  const totalTrades = (data.totalTrades as number) || 0;
  
  return `
  <div class="section">
    <h2>${section.title}</h2>
    <p><strong>Symbol:</strong> ${symbol} | <strong>Period:</strong> ${dateRange}</p>
    <p><strong>Total Trades:</strong> ${totalTrades} | <strong>Net Profit:</strong> 
      <span class="${netProfit >= 0 ? 'positive' : 'negative'}">₹${netProfit.toLocaleString()}</span>
    </p>
  </div>`;
}

function generateKPIsSection(section: ReportSection, data: Record<string, unknown>, branding: ReportBranding): string {
  const kpis = [
    { label: 'Net Profit', value: `₹${((data.netProfit as number) || 0).toLocaleString()}`, isPositive: (data.netProfit as number) >= 0 },
    { label: 'Win Rate', value: `${((data.winRate as number) || 0).toFixed(1)}%`, isPositive: (data.winRate as number) >= 50 },
    { label: 'Profit Factor', value: ((data.profitFactor as number) || 0).toFixed(2), isPositive: (data.profitFactor as number) >= 1.5 },
    { label: 'Sharpe Ratio', value: ((data.sharpeRatio as number) || 0).toFixed(2), isPositive: (data.sharpeRatio as number) >= 1 },
    { label: 'Max Drawdown', value: `${((data.maxDrawdown as number) || 0).toFixed(1)}%`, isPositive: (data.maxDrawdown as number) < 20 },
    { label: 'Total Trades', value: ((data.totalTrades as number) || 0).toString(), isPositive: true },
    { label: 'CAGR', value: `${((data.cagr as number) || 0).toFixed(1)}%`, isPositive: (data.cagr as number) > 0 },
    { label: 'Expectancy', value: ((data.expectancy as number) || 0).toFixed(2), isPositive: (data.expectancy as number) > 0 },
  ];
  
  return `
  <div class="section">
    <h2>${section.title}</h2>
    <div class="kpi-grid">
      ${kpis.map(kpi => `
        <div class="kpi-card">
          <div class="kpi-value ${kpi.isPositive ? 'positive' : 'negative'}">${kpi.value}</div>
          <div class="kpi-label">${kpi.label}</div>
        </div>
      `).join('')}
    </div>
  </div>`;
}

