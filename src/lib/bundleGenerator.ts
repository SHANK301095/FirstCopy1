export const generateBundle = async (
  batch: Batch,
  eas: EA[],
  onProgress?: (progress: number) => void
): Promise<void> => {
  const zip = new JSZip();
  const configsFolder = zip.folder('configs');
  const reportsFolder = zip.folder('reports');
  const logsFolder = zip.folder('logs');
  const csvFolder = zip.folder('csv');

  // Generate jobs
  const jobs: { ea: EA; symbol: string; timeframe: string }[] = [];
  for (const eaId of batch.eaIds) {
    const ea = eas.find((e) => e.id === eaId);
    if (!ea) continue;

    for (const symbol of batch.settings.symbols) {
      for (const timeframe of batch.settings.timeframes) {
        jobs.push({ ea, symbol, timeframe });
      }
    }
  }

  // Generate INI files
  jobs.forEach((job, index) => {
    const iniContent = generateIniContent(
      job.ea,
      job.symbol,
      job.timeframe,
      batch.settings,
      index
    );
    const fileName = `${String(index + 1).padStart(4, '0')}_${job.ea.name}_${job.symbol}_${job.timeframe}.ini`;
    configsFolder?.file(fileName, iniContent);
    onProgress?.((index / jobs.length) * 50);
  });

  // Generate scripts
  zip.file('run_backtests.ps1', generatePowerShellScript(jobs, batch.settings));
  zip.file('run_backtests.bat', generateBatWrapper());
  zip.file('README.md', generateReadme(batch.name, jobs, batch.settings));

  // Add placeholder files
  reportsFolder?.file('.gitkeep', '');
  logsFolder?.file('.gitkeep', '');
  csvFolder?.file('.gitkeep', '');

  onProgress?.(75);

  // Generate ZIP
  const content = await zip.generateAsync({ 
    type: 'blob',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 }
  });

  onProgress?.(100);

  // Download
  const safeName = batch.name.replace(/[^a-zA-Z0-9-_]/g, '_');
  saveAs(content, `${safeName}_bundle.zip`);
};
